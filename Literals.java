public class Literals {
    public static void main(String[] args) {

        int num = 0b111;
        System.out.println(num);

        int num1 = 1_00_00_000;
        System.out.println(num1);

        double num2 = 56;
        System.out.println(num2);

        double num3 = 12e10;
        System.out.println(num3);
    }
}
